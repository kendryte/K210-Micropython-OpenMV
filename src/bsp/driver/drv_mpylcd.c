static const uint8_t asc2_1608[];
static const uint8_t asc2_2412[];
static const uint8_t asc2_3216[];

static rt_device_t dev = 0;
static struct rt_device_graphic_info info;
static uint16_t fcolor = WHITE;
static int bcolor = BLACK;

static int lcd_init(void)
{
    int result;

    if (dev)
        return 0;

    dev = rt_device_find("lcd");
    if (!dev)
        return -1;

    if (rt_device_open(dev, 0) != 0)
    {
        dev = 0;
        return -1;
    }

    result = rt_device_control(dev, RTGRAPHIC_CTRL_GET_INFO, &info);
    if (result != 0)
    {
        rt_device_close(dev);
        dev = 0;
        return result;
    }

    return 0;
}

static int lcd_fill_point(int x, int y, uint16_t c)
{
    uint16_t *fb = (uint16_t *)info.framebuffer;

    if ((x < 0) || (y < 0) || (x >= info.width) || (y >= info.height))
        return -1;

    fb += x;
    fb += (y * info.width);

    *fb = c;

    return 0;
}

static void lcd_fill_char1608(int x, int y, char c, uint16_t color, int bc)
{
    uint8_t pos = 0;
    uint8_t t = 0;
    uint8_t data, temp;
    int size = 16;

    data = c - ' ';

    for (pos = 0; pos < size; pos++)
    {
        temp = asc2_1608[(uint16_t)data * size + pos];
        for (t = 0; t < 8; t++)
        {
            if (temp & 0x80)
                lcd_fill_point(x + t, y, color);
            else if (bc != -1)
                lcd_fill_point(x + t, y, (uint16_t)bc);

            temp <<= 1;
        }
        y++;
    }
}

static void lcd_fill_char2412(int x, int y, char c, uint16_t color, int bc)
{
    uint8_t pos = 0;
    uint8_t t = 0;
    uint8_t data, temp;
    int size = 24;
    int num;
    int x0;

    data = c - ' ';

    for (pos = 0; pos < (size * 16) / 8; pos++)
    {
        temp = asc2_2412[(uint16_t)data * (size * 16) / 8 + pos];
        if (pos % 2 == 0)
        {
            num = 8;
            x0 = x;
        }
        else
        {
            num = 4;
        }

        for (t = 0; t < num; t++)
        {
            if (temp & 0x80)
                lcd_fill_point(x0, y, color);
            else if (bc != -1)
                lcd_fill_point(x0, y, (uint16_t)bc);

            temp <<= 1;
            x0++;
        }

        if (pos % 2 != 0)
            y++;
    }
}

static void lcd_fill_char3216(int x, int y, char c, uint16_t color, int bc)
{
    uint8_t pos = 0;
    uint8_t t = 0;
    uint8_t data, temp;
    int size = 32;
    int x0;

    data = c - ' ';

    for (pos = 0; pos < size * (size / 2) / 8; pos++)
    {
        temp = asc2_3216[(uint16_t)data * size * (size / 2) / 8 + pos];
        if (pos % 2 == 0)
        {
            x0 = x;
        }

        for (t = 0; t < 8; t++)
        {
            if (temp & 0x80)
                lcd_fill_point(x0, y, color);
            else if (bc != -1)
                lcd_fill_point(x0, y, (uint16_t)bc);

            temp <<= 1;
            x0++;
        }

        if (pos % 2 != 0)
            y++;
    }
}

static void lcd_update(int x, int y, int w, int h)
{
    struct rt_device_rect_info rect;

    if (x < 0)
    {
        w += x;
        x = 0;
    }
    if (y < 0)
    {
        h += y;
        y = 0;
    }
    if ((x > info.width) || (y > info.height))
        return;

    if ((x + w) > info.width)
        w = info.width - x;
    if ((y + h) > info.height)
        h = info.height - h;

    if ((w < 1) || (h < 1))
        return;

    rect.x = x;
    rect.y = y;
    rect.width = w;
    rect.height = h;

    rt_device_control(dev, RTGRAPHIC_CTRL_RECT_UPDATE, &rect);
}

static void _draw_ellipse(int16_t x, int16_t y, int16_t rx, int16_t ry)
{
    int ix, iy;
    int h, i, j, k;
    int oh, oi, oj, ok;
    int xmh, xph, ypk, ymk;
    int xmi, xpi, ymj, ypj;
    int xmj, xpj, ymi, ypi;
    int xmk, xpk, ymh, yph;
    uint16_t color = fcolor;

    /*
     * Sanity check radii
     */
    if ((rx <= 0) || (ry <= 0))
        return;

    /*
     * Init vars
     */
    oh = oi = oj = ok = 0xFFFF;

    if (rx > ry)
    {
        ix = 0;
        iy = rx * 64;

        do
        {
            h = (ix + 32) >> 6;
            i = (iy + 32) >> 6;
            j = (h * ry) / rx;
            k = (i * ry) / rx;

            if (((ok != k) && (oj != k)) || ((oj != j) && (ok != j)) || (k != j))
            {
                xph = x + h;
                xmh = x - h;
                if (k > 0)
                {
                    ypk = y + k;
                    ymk = y - k;
                    lcd_fill_point(xmh, ypk, color);
                    lcd_fill_point(xph, ypk, color);
                    lcd_fill_point(xmh, ymk, color);
                    lcd_fill_point(xph, ymk, color);
                }
                else
                {
                    lcd_fill_point(xmh, y, color);
                    lcd_fill_point(xph, y, color);
                }
                ok = k;
                xpi = x + i;
                xmi = x - i;
                if (j > 0)
                {
                    ypj = y + j;
                    ymj = y - j;
                    lcd_fill_point(xmi, ypj, color);
                    lcd_fill_point(xpi, ypj, color);
                    lcd_fill_point(xmi, ymj, color);
                    lcd_fill_point(xpi, ymj, color);
                }
                else
                {
                    lcd_fill_point(xmi, y, color);
                    lcd_fill_point(xpi, y, color);
                }
                oj = j;
            }

            ix = ix + iy / rx;
            iy = iy - ix / rx;
        } while (i > h);
    }
    else
    {
        ix = 0;
        iy = ry * 64;

        do
        {
            h = (ix + 32) >> 6;
            i = (iy + 32) >> 6;
            j = (h * rx) / ry;
            k = (i * rx) / ry;

            if (((oi != i) && (oh != i)) || ((oh != h) && (oi != h) && (i != h)))
            {
                xmj = x - j;
                xpj = x + j;
                if (i > 0)
                {
                    ypi = y + i;
                    ymi = y - i;
                    lcd_fill_point(xmj, ypi, color);
                    lcd_fill_point(xpj, ypi, color);
                    lcd_fill_point(xmj, ymi, color);
                    lcd_fill_point(xpj, ymi, color);
                }
                else
                {
                    lcd_fill_point(xmj, y, color);
                    lcd_fill_point(xpj, y, color);
                }
                oi = i;
                xmk = x - k;
                xpk = x + k;
                if (h > 0)
                {
                    yph = y + h;
                    ymh = y - h;
                    lcd_fill_point(xmk, yph, color);
                    lcd_fill_point(xpk, yph, color);
                    lcd_fill_point(xmk, ymh, color);
                    lcd_fill_point(xpk, ymh, color);
                }
                else
                {
                    lcd_fill_point(xmk, y, color);
                    lcd_fill_point(xpk, y, color);
                }
                oh = h;
            }

            ix = ix + iy / ry;
            iy = iy - ix / ry;
        } while (i > h);
    }

    x -= rx;
    y -= ry;
    if (x < 0)
        x = 0;
    if (y < 0)
        y = 0;

    lcd_update(x, y, 2 * rx + 1, 2 * ry + 1);
}

void lcd_display_on(void)
{
    if (lcd_init() != 0)
        return;

    rt_device_control(dev, RTGRAPHIC_CTRL_POWERON, RT_NULL);
}

void lcd_display_off(void)
{
    if (lcd_init() != 0)
        return;

    rt_device_control(dev, RTGRAPHIC_CTRL_POWEROFF, RT_NULL);
}

void lcd_clear(int color)
{
    int x, y;

    if (lcd_init() != 0)
        return;

    for (x = 0; x < info.width; x++)
    {
        for (y = 0; y < info.height; y++)
        {
            lcd_fill_point(x, y, (uint16_t)color);
        }
    }

    lcd_update(0, 0, info.width, info.height);
}

void lcd_draw_point_color(int x, int y, int color)
{
    if (lcd_init() != 0)
        return;

    if (lcd_fill_point(x, y, (uint16_t)color) == 0)
        lcd_update(x, y, 1, 1);
}

void lcd_show_string(int x, int y, int size, const char *str)
{
    uint16_t color = fcolor;
    int bc = bcolor;
    int x0 = x;

    if (lcd_init() != 0)
        return;

    switch (size)
    {
    case 16:
    {
        while (*str)
        {
            lcd_fill_char1608(x, y, *str, color, bc);
            str++;
            x += 8;
        }

        lcd_update(x0, y, x - x0, size);
    }
    break;
    case 24:
    {
        while (*str)
        {
            lcd_fill_char2412(x, y, *str, color, bc);
            str++;
            x += 12;
        }

        lcd_update(x0, y, x - x0, size);
    }
    break;
    case 32:
    {
        while (*str)
        {
            lcd_fill_char3216(x, y, *str, color, bc);
            str++;
            x += 16;
        }

        lcd_update(x0, y, x - x0, size);
    }
    break;
    }
}

void lcd_draw_line(int x1, int y1, int x2, int y2)
{
    float k, b;
    int y, x;

    if (lcd_init() != 0)
        return;

    k = (y2 - y1) / (float)(x2 - x1);
    b = y1 - k * x1;

    for (x = x1; x <= x2; x++)
    {
        y = k * x + b;
        lcd_fill_point(x, y, fcolor);
    }

    lcd_update(x1, y1, x2 - x1 + 1, y2 - y1 + 1);
}

void lcd_draw_rectangle(int x1, int y1, int x2, int y2)
{
    int x, y;
    uint16_t color = (uint16_t)fcolor;

    if (lcd_init() != 0)
        return;

    for (x = x1; x < x2; x++)
    {
        lcd_fill_point(x, y1, color);
    }

    for (x = x1; x < x2; x++)
    {
        lcd_fill_point(x, y2, color);
    }

    for (y = y1; y < y2; y++)
    {
        lcd_fill_point(x1, y, color);
    }

    for (y = y1; y < y2; y++)
    {
        lcd_fill_point(x2, y, color);
    }

    lcd_update(x1, y1, x2 - x1 + 1, y2 - y1 + 1);
}

void lcd_draw_circle(int x1, int y1, int r)
{
    if (lcd_init() != 0)
        return;

    _draw_ellipse(x1, y1, r, r);
}

void lcd_set_color(int back, int fore)
{
    bcolor = back;
    fcolor = (uint16_t)fore;
}

void lcd_show_image(int x, int y, int length, int wide, const rt_uint8_t *buf)
{
    uint16_t *color = (uint16_t*)buf;
    int h, w;

    if (lcd_init() != 0)
        return;

    for (h = 0; h < length; h ++)
    {
        for (w = 0; w < wide; w ++)
        {
            lcd_fill_point(x + w, y + h, *color);
            color ++;
        }
    }

    lcd_update(x, y, wide, length);
}

